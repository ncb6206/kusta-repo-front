/**
 * 검색 관련 타입 정의
 * 검색 항목의 구조와 관련 인터페이스를 정의합니다
 */

/**
 * 검색 항목을 나타내는 인터페이스
 * 최근 검색어와 즐겨찾기에서 공통으로 사용됩니다
 */
export interface SearchItem {
  /** 검색 항목의 고유 식별자 */
  id: number;
  /** 검색어 텍스트 */
  text: string;
  /** 검색 항목이 생성된 시간 (타임스탬프) */
  createdAt: number;
  /** 선택적 이름 필드 (현재 사용되지 않음) */
  name?: number;
}
